# ─── 辅助函数 ────────────────────────────────────────────────────────────────
def get_text(el):
    return "".join(t.text or "" for t in el.iter(qn("w:t")))

def make_para(text, bold=False, indent=False, fs=10.5, align=None, italic=False):
    p = doc.add_paragraph()
    p._element.getparent().remove(p._element)
    if align:
        p.alignment = align
    if indent:
        p.paragraph_format.first_line_indent = Cm(0.74)
    if text:
        r = p.add_run(text)
        r.font.size = Pt(fs)
        r.bold = bold
        r.italic = italic
    return p._element

def find_para(keyword, start=0):
    for i, el in enumerate(list(body)):
        if i < start:
            continue
        if el.tag.endswith("}p") and keyword in get_text(el):
            return i, el
    return None, None

def insert_after(new_els, anchor_el):
    for el in reversed(new_els):
        anchor_el.addnext(el)

def replace_para_text(el, new_text):
    """Replace all text in a paragraph element"""
    for t_el in el.iter(qn("w:t")):
        t_el.text = ""
    runs = el.findall(f".//{qn('w:r')}")
    if runs:
        first_run = runs[0]
        for r_el in runs[1:]:
            r_el.getparent().remove(r_el)
        t_els = first_run.findall(qn("w:t"))
        if t_els:
            t_els[0].text = new_text
        else:
            t_new = OxmlElement("w:t")
            t_new.text = new_text
            first_run.append(t_new)

