# ----------- helpers -----------

def get_text(el):
    return "".join(t.text or "" for t in el.iter(qn("w:t")))


def make_para(text, bold=False, indent=False, fs=10.5, align=None, color=None):
    p = doc.add_paragraph()
    p._element.getparent().remove(p._element)
    if align:
        p.alignment = align
    if indent:
        p.paragraph_format.first_line_indent = Cm(0.74)
    if text:
        r = p.add_run(text)
        r.font.size = Pt(fs)
        r.bold = bold
        if color:
            r.font.color.rgb = RGBColor(*color)
    return p._element


def add_border(tbl_obj, color="999999"):
    tblPr = tbl_obj._tbl.tblPr
    tb = OxmlElement("w:tblBorders")
    for bn in ("top", "left", "bottom", "right", "insideH", "insideV"):
        b = OxmlElement(f"w:{bn}")
        b.set(qn("w:val"), "single")
        b.set(qn("w:sz"), "4")
        b.set(qn("w:space"), "0")
        b.set(qn("w:color"), color)
        tb.append(b)
    tblPr.append(tb)


def set_shade(cell, fill):
    tc = cell._tc
    tcPr = tc.get_or_add_tcPr()
    shd = OxmlElement("w:shd")
    shd.set(qn("w:val"), "clear")
    shd.set(qn("w:color"), "auto")
    shd.set(qn("w:fill"), fill)
    tcPr.append(shd)


def make_table(headers, rows, cw, hdr_color="1A5276", fs=9.5, bold_c0=False):
    t = doc.add_table(rows=1, cols=len(headers))
    t._element.getparent().remove(t._element)
    t.alignment = WD_TABLE_ALIGNMENT.CENTER
    add_border(t)
    for i, h in enumerate(headers):
        c = t.rows[0].cells[i]
        c.text = h
        c.width = Inches(cw[i])
        p = c.paragraphs[0]
        p.alignment = WD_ALIGN_PARAGRAPH.CENTER
        run = p.runs[0]
        run.bold = True
        run.font.size = Pt(fs)
        run.font.color.rgb = RGBColor(255, 255, 255)
        set_shade(c, hdr_color)
    for ri, row in enumerate(rows):
        rc = t.add_row().cells
        bg = "FFFFFF" if ri % 2 == 0 else "F2F4F7"
        for j, v in enumerate(row):
            rc[j].text = str(v)
            rc[j].width = Inches(cw[j])
            p = rc[j].paragraphs[0]
            p.alignment = WD_ALIGN_PARAGRAPH.CENTER
            if p.runs:
                p.runs[0].font.size = Pt(fs)
                if bold_c0 and j == 0:
                    p.runs[0].bold = True
            set_shade(rc[j], bg)
    return t._element


def find_para(keyword, start=0):
    for i, el in enumerate(list(body)):
        if i < start:
            continue
        if el.tag.endswith("}p") and keyword in get_text(el):
            return i, el
    return None, None


